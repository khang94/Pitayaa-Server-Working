package pitayaa.nail.msg.business.exception;

public class InvalidateFileTypeException extends Exception {
	private static final long serialVersionUID = -6061510811274815193L;

	public InvalidateFileTypeException() {
	}

	public InvalidateFileTypeException(String arg0) {
		super(arg0);
	}

	public InvalidateFileTypeException(Throwable arg0) {
		super(arg0);
	}

	public InvalidateFileTypeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}